import re

def find_and_sort_surnames(text: str) -> list[str]:
    """
    Находит в тексте все фамилии, соответствующие шаблону, и возвращает
    отсортированный по алфавиту список.

    Фамилией считается слово (или два через дефис) с заглавной буквы,
    за которым следуют инициалы формата "А.Б.".

    Args:
        text: Входной текст для анализа.

    Returns:
        Отсортированный список найденных фамилий.
    """

    pattern = r'([А-ЯЁ][а-яё]+(?:-[А-ЯЁ][а-яё]+)?)\s+(?:[А-ЯЁ]\.)+'
    
    surnames = re.findall(pattern, text)
    surnames.sort()
    
    return surnames

